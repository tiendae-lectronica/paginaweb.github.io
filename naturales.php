<p><h4><br>
Durante la primera mitad de el siglo xx la economia de Honduras fue dominada por empresas estado unidenses como la Standarfruit company 
la cual  establecio enorme plantaciones de banano a lo largo de la costa norte esta empresa iso rapidamente la exportacion de el producto con virtiendolo en uno de los mas exportados de el pais
De esta forma, el capital extranjero, la vida en las plantaciones de banano, y los conservadores fueron factores determinantes en la politica de hondurasdesde 1988 hasta mediados de el siglo xx.

</p>
<p><br>
Maanuel bonilla llego ala precidencia de Honduras , por  medio de las armas . una vez en el poder resulto hacer mejor amigo de las compañias bananeras que su antecesor, Terencio Sierra. Durante su administracion
estas empresas ganaron las extensiones de impuestos y el permiso para la construccion de muelles y carreteras asi como el permiso para obtener las cartas para la construccion del nuevo ferrocarril nacional de Honduras.
</p>
<p><br>
Durante la administracion de Miguel Rafael Davila Cuellar, Estados Unidos trato de ponerle fin a los conflictos centroamericanos atravez del tratado de paz y amistad centroamericana
